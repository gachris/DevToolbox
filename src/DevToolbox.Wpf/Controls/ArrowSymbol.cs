namespace DevToolbox.Wpf.Controls;

/// <summary>
/// Defines the types of arrow symbols that can be used in a diagram connection.
/// </summary>
public enum ArrowSymbol
{
    /// <summary>
    /// No arrow symbol is used.
    /// </summary>
    None = 0,

    /// <summary>
    /// A standard arrow symbol.
    /// </summary>
    Arrow = 1,

    /// <summary>
    /// A diamond-shaped symbol.
    /// </summary>
    Diamond = 2
}