namespace DevToolbox.Wpf.Controls;

/// <summary>
/// Specifies the type of mouse event that will trigger the expand or collapse action in an Expander control.
/// </summary>
public enum ExpanderMouseEventType
{
    /// <summary>
    /// Expands or collapses the Expander on a single mouse click.
    /// </summary>
    Click,

    /// <summary>
    /// Expands or collapses the Expander on a double mouse click.
    /// </summary>
    DoubleClick
}